package actions

import (
	"fmt"
	"log"
	"os"
	"os/exec"
)

type GoEnvAction struct {
	RunFile string
}

func (act GoEnvAction) DoAction() error {
	currDir, err := os.Getwd()
	if err != nil {
		return err
	}

	fmt.Printf("Making go environment in %s...\n", currDir)
	if err := act.makeRunScript(); err != nil {
		return err
	}
	if err := act.permitRunScript(); err != nil {
		return err
	}
	act.makeMainFile()

	return nil
}

func (act *GoEnvAction) makeRunScript() error {
	// Run file creation and writing
	f, err := os.Create("./" + act.RunFile + ".sh")
	defer f.Close()
	if err != nil {
		return err
	}
	f.WriteString("clear &&\ngo build -o ./a.out &&\n./a.out")

	return nil
}

func (act *GoEnvAction) permitRunScript() error {
	// Run file permissions
	cmd := exec.Command("chmod", "+x", "./"+act.RunFile+".sh")
	if err := cmd.Run(); err != nil {
		return err
	}

	return nil
}

func (act *GoEnvAction) makeMainFile() {
	file := "main.go"
	if FileExists(file) {
		fmt.Printf("%s found, skipping generation...", file)
		return
	}
	f, err := os.Create("./" + file)
	defer f.Close()
	if err != nil {
		log.Fatalf("Error creating main.go file: %s", err)
	}
	f.WriteString("package main\n\nimport \"fmt\"\n\nfunc main() {\n    fmt.Println(\"generated by jenv\")\n}")
}

func (act GoEnvAction) GetHelp() string {
	return "builds a go environment in the current directory"
}
